#include "3-calc.h"
/**
*op_add - adds two integers
* @a: the first int
*@b: the second int
*Return: the sum
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
*op_sub - subtracts two integers
*@a: the first int
*@b: the second int
*Return: the difference
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
*op_mul - mult two integers
*@a: the first int
*@b: the second int
*Return: the product
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
*op_div - div two integers
*@a: the first int
*@b: the second int
*Return: the div
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
*op_mod - mod two integers
*@a: the first int
*@b: the second int
*Return: the mod
*/
int op_mod(int a, int b)
{
return (a % b);
}
